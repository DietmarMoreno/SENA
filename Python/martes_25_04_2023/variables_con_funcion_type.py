#tipos de variables caracter
a="123"
print("caracter: ",type(a))
#tipos de variables numeros
b=123
print("numeral: ",type(b))
#tipos de varables con coma decimales
c=1,23
print("decimales con coma: ",type(c))
#tipos de varables con punto decimales
d=1.23
print("decimales con punto: ",type(d))
#tipos de varables true/false
e=True
print("decimales con punto: ",type(e))
#listado
obj=[1,2,3,4,5]
print(type(obj))